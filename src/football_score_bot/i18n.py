def normalize_language(lang: str | None, default: str = "zh") -> str:
    raw = (lang or default or "zh").strip()
    lowered = raw.lower()
    if lowered in {"en", "en-us", "en-gb"}:
        return "en"
    if lowered.startswith("zh") or lowered in {"cn", "zh-cn", "zh-hans", "zh-tw", "zh-hant"}:
        return "zh"
    return raw if raw in SUPPORTED_LANGUAGES else "zh"


def t(lang: str | None, key: str, **kwargs: object) -> str:
    normalized = normalize_language(lang)
    template = TRANSLATIONS.get(normalized, TRANSLATIONS["zh"]).get(key)
    if template is None:
        template = TRANSLATIONS["zh"].get(key, key)
    return template.format(**kwargs)


