def normalize_language(lang: str | None, default: str = "zh-CN") -> str:
    if lang in SUPPORTED_LANGUAGES:
        return lang
    if lang and lang.lower().startswith("zh"):
        return "zh-TW" if "tw" in lang.lower() or "hk" in lang.lower() else "zh-CN"
    return default if default in SUPPORTED_LANGUAGES else "zh-CN"


def t(lang: str | None, key: str, **kwargs: object) -> str:
    normalized = normalize_language(lang)
    template = MESSAGES.get(normalized, MESSAGES["zh-CN"]).get(key)
    if template is None:
        template = MESSAGES["zh-CN"].get(key, key)
    return template.format(**kwargs)
